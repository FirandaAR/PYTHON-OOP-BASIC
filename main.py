def nilai(nama, uts, uas):
    rumus = uts + uas / 2
    print(f"Nilai Akhir {nama}: {rumus}")

nilai("Aisy", 80, 100)
nilai("Zahran", 85, 96)
nilai("Firanda", 40, 86)